    private void cAgendaItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_cAgendaItemStateChanged
        String nom =  (String) cAgenda.getSelectedItem();
        Contacto contacto = agenda.obtenerContacto(nom);
        
        // COMPROBAR QUE EL CONTACTO NO ESTE VACIO
        if (contacto != null ) {
            tfNombre.setText(contacto.getNombre());
            tfDireccion.setText(contacto.getDireccion());
            tfLocalidad.setText(contacto.getLocalidad());
            tfTelefono.setText(contacto.getTelefono());
            tfCorreo.setText(contacto.getCorreo());
            tfEdad.setText(""+contacto.getEdad());            
        }
        tfNombre.requestFocus();
    }//GEN-LAST:event_cAgendaItemStateChanged

