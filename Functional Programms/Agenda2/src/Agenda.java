    private void bVisualizarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bVisualizarActionPerformed
        modelo.setRowCount(0);
        modelo.setColumnCount(0);
        modelo.addColumn("Nombre");
        modelo.addColumn("Dirección");
        modelo.addColumn("Localidad");
        modelo.addColumn("Teléfono");
        modelo.addColumn("Correo");
        modelo.addColumn("Edad");        
        Contacto nuevoContacto;
            
        for(int i=0;i < cAgenda.getItemCount();i++){
            nuevoContacto=agenda.obtenerContacto((String) cAgenda.getItemAt(i));
            modelo.addRow(new Object[]{nuevoContacto.getNombre(), nuevoContacto.getDireccion(), nuevoContacto.getLocalidad(),nuevoContacto.getTelefono(),nuevoContacto.getCorreo(),nuevoContacto.getEdad()});
        }
        dAgenda.setVisible(true);        
    }//GEN-LAST:event_bVisualizarActionPerformed
    
    public void borrarContacto(){
        String nombre = tfNombre.getText();
        String direccion = tfDireccion.getText();
        String localidad = tfLocalidad.getText();
        String telefono = tfTelefono.getText();
        String correo = tfCorreo.getText();
        int edad = Integer.parseInt( tfEdad.getText() );
                
        Contacto nuevoContacto = new Contacto(nombre,direccion,localidad,telefono,correo,edad);
        
        agenda.borrarContacto(nuevoContacto);
        
        rellenarListaDesplegable();
    }
    
    public void insertarContacto(){
        String nombre = tfNombre.getText();
        String direccion = tfDireccion.getText();
        String localidad = tfLocalidad.getText();
        String telefono = tfTelefono.getText();
        String correo = tfCorreo.getText();
        int edad = Integer.parseInt( tfEdad.getText() );
        
        Contacto nuevoContacto = new Contacto(nombre,direccion,localidad,telefono,correo,edad);
        
        agenda.insertarContacto(nuevoContacto);
        
        rellenarListaDesplegable();
    }
    
    public void modificarContacto(){
        String nombre = tfNombre.getText();
        String direccion = tfDireccion.getText();
        String localidad = tfLocalidad.getText();
        String telefono = tfTelefono.getText();
        String correo = tfCorreo.getText();
        int edad = Integer.parseInt( tfEdad.getText() );
        
        String nombreAuxiliar=(String) cAgenda.getSelectedItem();
        
        Contacto nuevoContacto = new Contacto(nombre,direccion,localidad,telefono,correo,edad);
        
        agenda.modificarContacto(nuevoContacto, nombreAuxiliar);
        
        rellenarListaDesplegable();
    }
    
    
