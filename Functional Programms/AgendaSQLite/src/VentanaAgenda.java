    private void chSeleccionarItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_chSeleccionarItemStateChanged
        String nom = (String) chSeleccionar.getSelectedItem();
        Contacto contacto = agenda.obtenerContacto(nom);
        if (contacto != null) {
            tfNom.setText(contacto.getNombre());
            tfDir.setText(contacto.getDireccion());
            tfEmail.setText(contacto.getEmail());
            tfTlf.setText(contacto.getTelefono());
            tfEdad.setText("" + contacto.getEdad());
            tfLoc.setText(contacto.getLocalidad());
        }
        tfNom.requestFocus();
    }//GEN-LAST:event_chSeleccionarItemStateChanged

