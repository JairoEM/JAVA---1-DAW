    public void rellenarListaDesplegable() {
        ResultSet res = agenda.obtenerRegistros("Contactos");
        try {
            if (res != null) {
                chSeleccionar.removeAllItems();
                while (res.next()) {
                    chSeleccionar.addItem(res.getString("Nombre"));
                }
            }
        } catch (SQLException e) {
            System.out.println("Error rellenar lista.");
        }
    }

