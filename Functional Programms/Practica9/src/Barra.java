public class Barra {
    private String codigoBarra, barra;

    public Barra(String codigoBarra, String barra) {
        this.codigoBarra = codigoBarra;
        this.barra = barra;
    }

    public String getCodigoBarra() {
        return codigoBarra;
    }

    public String getBarra() {
        return barra;
    }  
}
