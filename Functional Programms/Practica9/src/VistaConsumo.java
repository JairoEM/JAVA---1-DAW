    public void rellenarComboBox(){
        ResultSet resultado = controlador.obtenerRegistros("Barras");
        try {
            if(resultado!=null){
                while(resultado.next()){
                    cbBarra.addItem(resultado.getString("barra"));
                }
            }
        } catch (Exception e) {
            System.out.println("Fallo al insertar las barras");
        }
    }
    
    public void rellenarTabla(){      
        ResultSet resultado=controlador.rellenarTabla(tfFecha.getText(), (String) cbBarra.getSelectedItem());
        
        modelo.setColumnCount(0);
        modelo.setRowCount(0);
        modelo.addColumn("Art√≠culo");
        modelo.addColumn("Unidades");
        modelo.addColumn("PVP");
        modelo.addColumn("Importe");
        
        float total=0;
        float importe=0;
        try {
            if(resultado!=null){
                while(resultado.next()){
                    importe=resultado.getInt("unidades")*resultado.getFloat("pvp");
                    modelo.addRow(new Object[]{resultado.getString("articulo"),
                        resultado.getString("unidades"), resultado.getString("pvp"), importe});
                    total+=importe;
                }
            }
            lEditable.setText(""+total);
        } catch (Exception e) {
            System.out.println("Error al rellenar la tabla desde la vista");
        }
    }
    
