    class Cuenta extends Thread{
        public Cuenta(String nom){super(nom);}
        public void run(){
            try{
                num1=Integer.parseInt(tfContar.getText());
            }catch(NumberFormatException e){
                num1=0;
            }
            while(num1 <= 100 && Thread.currentThread()==hilo1) {
                tfContar.setText("" + num1);
                try {                  
                    Thread.sleep(1000);                   
                } catch (InterruptedException ex) {}
                num1++;
            }
        }
    }
    
    class Descuenta extends Thread{
        public Descuenta(String nom){super(nom);}
        public void run(){
            try{
                num2=Integer.parseInt(tfDescontar.getText());
            }catch(NumberFormatException e){
                num2=100;
            }
            while(num2 >= 0 && Thread.currentThread()==hilo2) {
                tfDescontar.setText("" + num2);
                try {                   
                    Thread.sleep(1000);                   
                } catch (InterruptedException ex) {}
                num2--;
            }
        }
    }
    
    
    public void nuevoHilo1() {
        if(hilo1==null) {
            hilo1 = new Cuenta("Hilo Cuenta");
            hilo1.start();      
        }
    }
    
    public void nuevoHilo2() {
        if(hilo2==null) {
            hilo2 = new Descuenta("Hilo Descuenta");
            hilo2.start();      
        }
    }
    
    public void finalizarHilo1() {
        hilo1=null;  
    }
    
    public void finalizarHilo2() {
        hilo2=null;  
    }
    
    
