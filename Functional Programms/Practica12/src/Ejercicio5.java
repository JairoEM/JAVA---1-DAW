    public void hora() throws InterruptedException{
        int i=1;
        while (i<=1000000000 && Thread.currentThread()==hilo){
            Thread.sleep(1000);
            nSegundos++;
            if(nSegundos>=60){
                nSegundos=0;
                nMinutos++;
                if(nMinutos>=60){
                    nHoras++;
                    nMinutos=0;
                    if(nHoras>=24){
                        nHoras=0;
                    }
                }
            }
            tfHora.setText(nHoras+":"+nMinutos+":"+nSegundos);
            i++;
        }
    }
    
    public void run(){
        try{
            hora();
        }catch(InterruptedException e){}
    }
