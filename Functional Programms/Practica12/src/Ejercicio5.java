    private void bAplicarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_bAplicarActionPerformed
        try{
            nHoras=Integer.parseInt(tfDH.getText());        
        }catch(NumberFormatException e){
            nHoras=0;
        }
        if(nHoras>=24){
            nHoras=0;
        }
        
        try{
            nMinutos=Integer.parseInt(tfDM.getText());
        }catch(NumberFormatException e){
            nMinutos=0;
        }
        if(nMinutos>=60){
            nMinutos=0;
        }
        
        try{
            nSegundos=Integer.parseInt(tfDS.getText());
        }catch(NumberFormatException e){
            nSegundos=0;
        }
        if(nSegundos>=60){
            nSegundos=0;
        }
        
        tfDH.setText(null);
        tfDM.setText(null);
        tfDS.setText(null);
        
        dAlerta.setVisible(false);
        
        hilo=new Thread(this);
        hilo.start();
    }//GEN-LAST:event_bAplicarActionPerformed

