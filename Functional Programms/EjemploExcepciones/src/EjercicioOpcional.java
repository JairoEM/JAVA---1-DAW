    private void tfNotaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_tfNotaActionPerformed
        try{
            double auxSum=Mate.insertarNumero(Double.parseDouble(tfNota.getText()));
            sum=sum+auxSum;
            tfNota.setText(null);
            tfNota.requestFocus();
            cont++;
            tfContador.setText(""+cont);
        }catch(NumberFormatException e){
            dAlerta.pack();
            dAlerta.setVisible(true);
        }catch(RangoNumeroException e){
            dAlerta.pack();
            dAlerta.setVisible(true);
        }
    }//GEN-LAST:event_tfNotaActionPerformed

